GRADING_CRITERIA = """
EA - Explanation Accurate
ES - Explanation Selective
EC - Explanation Clear
FA - Fixes Accurate
FS - Fixes Selective
FC - Fixes Clear
"""

GRADING_GUIDELINES = """
- EA (Explanation Accurate): The explanation correctly identifies and explains the true bug(s) in the student program.
- ES (Explanation Selective): The explanation does not mention non-existent or irrelevant bugs.
- EC (Explanation Clear): The explanation is easy for a novice programmer to understand.
- FA (Fixes Accurate): The fixes correctly describe the necessary changes to repair the actual bug(s).
- FS (Fixes Selective): The fixes do not include unnecessary or irrelevant modifications.
- FC (Fixes Clear): The fixes are clear, concise, and uniquely identify the required changes.
"""